void LCD_draw_line(uint16 x0, uint16 y0, uint16 x1, uint16 y1, uint16 color) {
    int16 dx = ABS((int16)x1 - (int16)x0);
    int16 sx = x0 < x1 ? 1 : -1;
    int16 dy = -ABS((int16)y1 - (int16)y0);
    int16 sy = y0 < y1 ? 1 : -1;
    int16 err = dx + dy;
    int16 e2;

    while (1) {
        // draw the current pixel
        LCD_write_pixel_fast(x0, y0, color); 
        
        // if we reached the final coordinate, break the loop
        if (x0 == x1 && y0 == y1) break;
        
        // calculate the error and take the next step
        e2 = 2 * err;
        if (e2 >= dy) { 
            err += dy; 
            x0 += sx; 
        }
        if (e2 <= dx) { 
            err += dx; 
            y0 += sy; 
        }
    }
}

void LCD_graph(uint16 *volts, uint16 *amps, uint32 num_points) {
    // initialize axes
    LCD_draw_axes(); 
    LCD_print(310, 470, "V", COLOR_WHITE, COLOR_BLACK, 1);
    LCD_print(18, 285, "I", COLOR_WHITE, COLOR_BLACK, 1);
    
    if (num_points == 0) return; // safety check
    
    // find the max values for auto-scaling
    uint16 max_volt = volts[0];
    uint16 max_amps = amps[0];
    uint16 min_volt = volts[0];
    uint16 min_amps = amps[0];
    
    uint16 j;
    for(j = 0; j < num_points; j++) {
        max_volt = (volts[j] > max_volt) ? volts[j] : max_volt; 
        max_amps = (amps[j] > max_amps) ? amps[j] : max_amps; 
        
        min_volt = (volts[j] < min_volt) ? volts[j] : min_volt; 
        min_amps = (amps[j] < min_amps) ? amps[j] : min_amps; 
    }
    
    // safety check to not divide by 0
    if (max_volt == 0) max_volt = 1;
    if (max_amps == 0) max_amps = 1;
    
    CS_Write(0);
    
    uint16 x_pixel; 
    uint16 y_pixel; 
    uint16 x_prev = 0; // Keep track of the previous X
    uint16 y_prev = 0; // Keep track of the previous Y
    
    // plotting loop using clean array indexing
    uint16 i;
    for(i = 0; i < num_points; i++) {
        // explicitly cast to uint32 before multiplying to prevent overflow
        x_pixel = X_ORIGIN + ((uint32)volts[i] * 259) / max_volt; 
        y_pixel = Y_ORIGIN - ((uint32)amps[i] * 167) / max_amps; 
        
        if (i == 0) {
            // draw a single dot for the very first point
            LCD_write_pixel_fast(x_pixel, y_pixel, COLOR_GREEN);
        } else {
            // draw a continuous line from the previous point to the new point
            LCD_draw_line(x_prev, y_prev, x_pixel, y_pixel, COLOR_GREEN);
        }
        
        // update previous coordinates for the next iteration
        x_prev = x_pixel;
        y_prev = y_pixel;
    }
    
    LCD_print(10, 240, "x min/max:", COLOR_WHITE, COLOR_BLACK, 2);
    LCD_print(10, 260, "y min/max:", COLOR_WHITE, COLOR_BLACK, 2);
    
    char range[12];
    uint32ToString(min_volt, range); 
    LCD_print(160, 240, range, COLOR_WHITE, COLOR_BLACK, 2); 
    uint32ToString(max_volt, range); 
    LCD_print(240, 240, range, COLOR_WHITE, COLOR_BLACK, 2); 
    uint32ToString(min_amps, range); 
    LCD_print(160, 260, range, COLOR_WHITE, COLOR_BLACK, 2); 
    uint32ToString(max_amps, range); 
    LCD_print(240, 260, range, COLOR_WHITE, COLOR_BLACK, 2); 
    CS_Write(1);
}

